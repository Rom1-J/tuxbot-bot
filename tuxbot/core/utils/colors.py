"""Discord status colors."""

import discord


class Colors:
    """Enum of all different discord status colors."""

    ONLINE: discord.Color = discord.Color(0x3BA55D)
    IDLE: discord.Color = discord.Color(0xFAA81A)
    DND: discord.Color = discord.Color(0xED4245)
    STREAMING: discord.Color = discord.Color(0x593696)
    EMBED_BORDER: discord.Color = discord.Color(0x2F3136)
